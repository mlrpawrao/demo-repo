# Simple madlibs example with a sentence.
exclamation=input("Enter an exclamation expression: -")
adverb=input("Enter a verb/adverb: -")
noun=input("Enter a noun: -")
adjective=input("Enter an adjective: -")
print(" You have entered all the required fields for madlibs")

print(f"{exclamation}! he said {adverb} as he jumped into his convertible {noun} and drove off with his {adjective}.")